function setText(id, value) {
  const element = document.getElementById(id);
  if (element) {
    element.textContent = value;
  }
}

function setOptionalText(id, value) {
  const element = document.getElementById(id);
  if (!element) {
    return;
  }

  element.textContent = value || "";
  element.hidden = !value;
}

function renderFacts(locale) {
  const container = document.getElementById("fact-grid");
  container.innerHTML = locale.hero.facts
    .map(
      (item) => `
        <div class="fact-item">
          <dt>${item.label}</dt>
          <dd>${item.value}</dd>
        </div>
      `
    )
    .join("");
}

function renderAbout(locale) {
  const container = document.getElementById("about-copy");
  container.innerHTML = locale.about.paragraphs
    .map((paragraph) => `<p>${paragraph}</p>`)
    .join("");
}

function renderInterests(locale) {
  const container = document.getElementById("interest-list");
  container.innerHTML = locale.about.interests
    .map((interest) => `<span class="interest-chip">${interest}</span>`)
    .join("");
}

function renderAgenda(locale) {
  const container = document.getElementById("agenda-list");
  container.innerHTML = locale.agenda.items
    .map(
      (item, index) => `
        <article class="agenda-card reveal">
          <span class="agenda-index">${String(index + 1).padStart(2, "0")}</span>
          <h3>${item.title}</h3>
          <p>${item.description}</p>
        </article>
      `
    )
    .join("");
}

