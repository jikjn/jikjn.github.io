function setText(id, value) {
  const element = document.getElementById(id);
  if (element) {
    element.textContent = value;
  }
}

function renderFacts(locale) {
  const container = document.getElementById("fact-grid");
  container.innerHTML = locale.hero.facts
    .map(
      (item) => `
        <div class="fact-item">
          <dt>${item.label}</dt>
          <dd>${item.value}</dd>
        </div>
      `
    )
    .join("");
}

function renderAbout(locale) {
  const container = document.getElementById("about-copy");
  container.innerHTML = locale.about.paragraphs
    .map((paragraph) => `<p>${paragraph}</p>`)
    .join("");
}

function renderInterests(locale) {
  const container = document.getElementById("interest-list");
  container.innerHTML = locale.about.interests
    .map((interest) => `<span class="interest-chip">${interest}</span>`)
    .join("");
}

function renderAgenda(locale) {
  const container = document.getElementById("agenda-list");
  container.innerHTML = locale.agenda.items
    .map(
      (item, index) => `
        <article class="agenda-card reveal">
          <span class="agenda-index">${String(index + 1).padStart(2, "0")}</span>
          <h3>${item.title}</h3>
          <p>${item.description}</p>
        </article>
      `
    )
    .join("");
}

function renderPublications(locale) {
  const container = document.getElementById("publication-list");
  container.innerHTML = PUBLICATIONS.map((publication) => {
    const titleTag = publication.hasTitleTag
      ? `<span class="publication-title-tag">${locale.publications.titleTag}</span>`
      : "";
    const tierTag = `<span class="publication-tier-tag">${locale.publications.tierTag}</span>`;

    const badges = publication.badges
      .map((badge) => {
        const label = locale.publications.badges[badge];
        const className = badge === "oral" ? "badge-oral" : "badge-main";
        return `<span class="badge ${className}">${label}</span>`;
      })
      .join("");

    const paperLink = publication.paperUrl
      ? `
        <div class="publication-links">
          <a class="publication-link" href="${publication.paperUrl}" target="_blank" rel="noreferrer">
            ${locale.publications.paperLabel}
          </a>
        </div>
      `
      : "";

    return `
      <article class="publication-card reveal">
        <div class="publication-year">${publication.year}</div>
        <div>
          <p class="publication-venue">${publication.venue}</p>
          <h3 class="publication-title">${titleTag}${publication.title}${tierTag}</h3>
          ${badges ? `<div class="publication-badges">${badges}</div>` : ""}
          ${paperLink}
        </div>
      </article>
    `;
  }).join("");
}

